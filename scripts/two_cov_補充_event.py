def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    conn = sqlite3.connect(DB)
    conn.execute("PRAGMA foreign_keys = ON")

    # 資料表欄位順序
    cols = ("event_id", "event_date", "event_type", "issue_tags", "actor_id",
            "title", "summary", "full_quote", "source_url", "source_archive",
            "related_pi", "related_co", "related_article",
            "is_positive_outcome", "note", "created_at")
    placeholders = ",".join(["?"] * len(cols))
    sql = f"INSERT OR IGNORE INTO event ({','.join(cols)}) VALUES ({placeholders})"

    inserted = 0
    skipped = 0
    seen_ids = set()
    for e in EVENTS:
        if e["event_id"] in seen_ids:
            print(f"  ⚠️ duplicate event_id in script: {e['event_id']} -> renaming")
            # 自動 dedup: append actor short hash
            e["event_id"] = f"{e['event_id']}-{e['actor_id'][-3:]}"
        seen_ids.add(e["event_id"])

        row = tuple(e[c] for c in cols)
        if args.dry_run:
            print(f"  [dry] {e['actor_id']} {e['event_id']} {e['event_date']} {e['title'][:40]}")
            inserted += 1
            continue
        cur = conn.execute(sql, row)
        if cur.rowcount > 0:
            inserted += 1
        else:
            skipped += 1

    if not args.dry_run:
        conn.commit()
    conn.close()

    print(f"\n=== Summary ===")
    print(f"Total events in script: {len(EVENTS)}")
    print(f"Inserted: {inserted}")
    print(f"Skipped (duplicate event_id in DB): {skipped}")


if __name__ == "__main__":
    main()
