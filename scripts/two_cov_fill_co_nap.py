def main():
    print(f"連線資料庫: {DB}")
    conn = sqlite3.connect(DB)
    cur = conn.cursor()

    # ----- Task 1: concluding_observation -----
    print("\n[Task 1] 填充 concluding_observation...")
    co_inserted = 0
    for row in CO_DATA:
        co_id, review, year, para, topic, covenant, articles, title, recom, status, pol_use, sel_count = row
        cur.execute("""
            INSERT OR REPLACE INTO concluding_observation
              (co_id, review, review_year, paragraph_number, topic, covenant,
               article_referenced, chinese_title, recommendation_full,
               follow_up_status, political_use_score, selective_citation_count,
               created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (co_id, review, year, para, topic, covenant,
              json.dumps(articles, ensure_ascii=False),
              title, recom, status, pol_use, sel_count, NOW, NOW))
        co_inserted += 1
    print(f"  ✓ 寫入 {co_inserted} 筆 CO")

    # ----- Task 2: nap_action -----
    print("\n[Task 2] 填充 nap_action...")
    nap_inserted = 0
    for row in NAP_DATA:
        nap_id, phase, period, num, cat, title, agency, co_ref, status, self_eval, civil, budget = row
        cur.execute("""
            INSERT OR REPLACE INTO nap_action
              (nap_id, phase, phase_period, action_number, category, short_title,
               responsible_agency, co_referenced, status, self_evaluation,
               civil_society_view, budget_allocated, created_at, updated_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (nap_id, phase, period, num, cat, title, agency,
              json.dumps(co_ref, ensure_ascii=False),
              status, self_eval, civil, budget, NOW, NOW))
        nap_inserted += 1
    print(f"  ✓ 寫入 {nap_inserted} 筆 NAP action")

    # ----- Task 3: law_amendment -----
    print("\n[Task 3] 補齊 law_amendment...")
    amd_inserted = 0
    for row in LAW_AMENDMENT_DATA:
        cur.execute("""
            INSERT OR REPLACE INTO law_amendment
              (amendment_id, law_id, version_id, related_event_id,
               proposal_date, first_reading, third_reading, promulgated,
               proposer_summary, contention_level,
               summary_before, summary_after, key_changes,
               related_executive_orders, triggered_by_co, note)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, row)
        amd_inserted += 1
    print(f"  ✓ 寫入 {amd_inserted} 筆 law_amendment")

    conn.commit()

    # 驗證
    print("\n=== 驗證 ===")
    cur.execute("SELECT COUNT(*) FROM concluding_observation")
    print(f"concluding_observation : {cur.fetchone()[0]} rows")
    cur.execute("SELECT COUNT(*) FROM nap_action")
    print(f"nap_action             : {cur.fetchone()[0]} rows")
    cur.execute("SELECT COUNT(*) FROM law_amendment")
    print(f"law_amendment          : {cur.fetchone()[0]} rows")

    # CO 分屆統計
    print("\n--- CO 分屆統計 ---")
    cur.execute("SELECT review, COUNT(*) FROM concluding_observation GROUP BY review")
    for r in cur.fetchall():
        print(f"  第 {r[0]} 屆 (review={r[0]}) : {r[1]} 筆")

    # NAP 分階段
    print("\n--- NAP 分階段統計 ---")
    cur.execute("SELECT phase, COUNT(*) FROM nap_action GROUP BY phase")
    for r in cur.fetchall():
        print(f"  NAP {r[0]} : {r[1]} 筆")

    conn.close()
    print("\n完成。")


if __name__ == "__main__":
    main()
